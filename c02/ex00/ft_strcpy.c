char	*ft_strcpy(char *dest, char *src)
{
	char	*p_d;
	char	*p_s;

	p_s = src;
	p_d = dest;
	while (*p_s)
	{
		*p_d = *p_s;
		p_d++;
		p_s++;
	}
	*p_d = '\0';
	return (dest);
}
